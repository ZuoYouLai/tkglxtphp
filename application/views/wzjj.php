                    <h1 class="intor_wz">
                        网站简介
                    </h1>
                    <p>
                        网站既庄重又相对活跃。整体设计采用党旗的色彩构成元素，以红、橙为主，版面设计体现湖南特色，体现中南特色，体现党校特色：图文并茂，色彩搭配协调，主页与子页风格一致，并辅以动画等手段。
                    </p>
                    <p>
                        网站使用技术成熟，主要是以Windows 2000 Advanced Server为平台，采用Microsoft SOL Server数据库管理技术，网站运行稳定。
                    </p>
                    <p>
                        网站运行以来，由于内容丰富、与党校培训工作结合紧密、交互性强、更新及时、管理到位，网站受到了入党积极分子的欢迎，访问量与日俱增。
                    </p>
                    <p>
                        受众（特别是入党积极分子）进入党校网站都能有所收获，有关疑难问题能得到解决，对学习党的"三基本"知识很有帮助；自2002年上半年起至今的 18期入党积极分子培训师生互动讨论活动中，每期提出的问题都在14000至19000个之间，给出的答案在25000-40000个之间。
                    </p>
                    <p>
                        通过互动讨论学习、解答热点问题，澄清了学员的模糊认识，巩固了培训班课堂所学知识，网站得到了入党积极分子和干部教师的好评，在青年大学生中的影响不断扩大。网站也得到学校和上级领导部门的充分肯定。
                    </p>
