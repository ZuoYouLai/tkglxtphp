                <div class="sec_list">
                    <h1 class="intor_wz">
                        网站简介
                    </h1>
                    <p>
                        中南大学党校"理想导航"网站为满足广大大学生进入党校学习，升华自己的政治理想以及学习马列主义、毛泽东思想、邓小平理论和"三个代表"重要思想的需要，在校党委的关怀下，经过精心筹备，于中国共产党成立八十周年之际（2001年6月28日）发布运行。
                    </p>
                    <p>
                        随着党校工作的不断发展，在运行过程中，为进一步满足学校党员干部教育培训的需要，逐步加强了服务党员干部远程学习的功能，新增加了中层干部在线学习中
                        心、党员在线学习等模块；同时，中南大学党校作为中国高校党校研究分会教学部的部长机构，网站为教学部机构的信息发布与工作交流提供平台支持。
                    </p>
                    <p>
                        网站面向中层干部、党员和入党积极分子培训班学员，兼顾其他要求入党的积极分子和青年学生的教育。旗帜鲜明，导向明确。
                    </p>
                    <p>
                        向广大党员、干部、入党积极分子介绍党的基本知识、基本路线、基本理论，解答热点问题，提供马克思主义学习资料，发布党校办学、党校教学研究等信息。
                    </p>
