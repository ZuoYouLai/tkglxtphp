                <div class="sec_list">
                    <h1 class="intor">
                        党校简介
                    </h1>
                    <p style="text-indent:2em;">
                        广东医学院党校成立于1985年，近30年来，在校党委的领导下，形成了完备的教育培训体 系，培训规模不断扩大，对推动 广东医学院的发展，起到了应有的作用。现有专、兼职教师80余人，其中湖南省教学名师4人，专职人员4人。截至2011年，每
                        年举办各类培训班130个班次，培训各类人员20000余人次。2006年荣获湖南省高校先进党校称号。
                    </p>
                    <div style="margin-left:40px;font-weight:bold;">
                        党校的宗旨：实事求是，理想导航
                        <br>
                        党校的目标：建成师生的精神家园
                        <br>
                        党校的价值理念：讲党性、说真话、求真理、育真人
                    </div>
                </div>
                <div class="sec_list line_top_b" style="background: url(../myimages/sec_list_bg.gif) no-repeat;">
                    <h1 class="part_a">
                        一、党校组织建制
                    </h1>
                    <p style="margin-left:110px;">
                        <img class="pic_info" src="dx_gdmc/sec_dx_tz.gif" alt="">
                    </p>
                    <p style="text-indent:2em;">
                        党校与组织部合署办公。级别与其它党群部门一样，为正处级。学校培训中心、社会主义学院、 党建研究所与党校四个机构一套人员。校党委副书记兼任党校校长，另配备专职常务副校长和专职副校长各一名，另还有两名专职人员。党校成立了由校党委各职能
                        部门负责人和政治学院负责人参与组成的校务委员会。
                    </p>
                    <p style="text-indent:2em;">
                        党校校务委员会协调学校有关教育培训工作。党校有专职管理人员（教师）四人，还聘任包括全 体校领导和部分院士在内的兼职教师70余名。培训工作与组织部分工合作，相对独立，经费单列、自主使用。组织部主要负责培训工作的设计规划，党校主要负责
                        具体培训。有全日制学生的学院设立分党校。分党校校长、副校长分别由二级党组织书记、副书记兼任，学院党员分团委书记兼任分党校办公室主任，分党校在二级
                        党组织领导下和在校党校的指导下开展工作。分党校的主要职责是协助校党校主办网上党校（入党积极分子初级班），学生2000人以上的学院分党校可办入党积
                        极分子高级班。
                    </p>
                    <p>
                        <img style="margin-left:-8px;" src="dx_gdmc/dxtz.jpg" alt="" width="711">
                    </p>
                </div>
                <div class="sec_list line_top_b">
                    <h1 class="part_b">
                        二、党校工作指导思想和教育理念
                    </h1>
                    <p>
                        坚持以邓小平理论和"三个代表"重要思想为指导深入贯彻落实科学发展观。党校既是培养党员、干部和入党积极分子的学校，又是党委的一个重要部门，是学习马
                        克思主义的重要阵地，是党员党性锻炼的熔炉，是研究党建和思想政治工作的重要基地，是哲学社会科学的研究机构。校党委始终不渝地重视和加强党校工作。
                        我们的理念是： 坚持理想导航，理想升华、理想起航，促进终身学习，把党校建成师生的"精神家园"。
                    </p>
                </div>
                <div class="sec_list line_top_b">
                    <h1 class="part_c">
                        三、工作目标
                    </h1>
                    <p>
                        将党校建成与学校地位和发展要求相适应的党性教育基地，为争创学习型党组织、学习型党员、学习型干部和培养更多的青年马克思主义者提供前沿理论阵地。
                    </p>
                </div>
                <div class="sec_list">
                    <h1 class="part_d">
                        四、工作原则
                    </h1>
                    <p>
                        党性教育与技能培训结合，理论与实际结合，重点对象与所有人员结合，组织推动与个人自学结合，培训与使用相结合。
                    </p>
                </div>
